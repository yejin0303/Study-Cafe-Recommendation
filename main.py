def rating_to_score(rating):

    if pd.isna(rating):
        return np.nan

    try:
        rating = float(rating)

    except Exception:
        return np.nan

    if rating <= 0:
        return np.nan

    return max(
        0,
        min(
            100,
            rating / 5 * 100
        )
    )


def review_confidence(review_count):

    if pd.isna(review_count):
        review_count = 0

    try:
        review_count = float(review_count)

    except Exception:
        review_count = 0

    return (
        0.7
        + 0.3 * min(
            1.0,
            review_count / 50
        )
    )


def weighted_study_sentiment_score(
    cafe_feature_rows
):

    if cafe_feature_rows.empty:
        return 50.0

    weighted_sum = 0.0
    used_weight = 0.0

    for feature, weight in FEATURE_WEIGHTS.items():

        row = cafe_feature_rows[
            cafe_feature_rows["feature"]
            == feature
        ]

        if row.empty:
            continue

        weighted_sum += (
            float(
                row["feature_score"]
                .iloc[0]
            )
            * weight
        )

        used_weight += weight

    if used_weight == 0:
        return 50.0

    return (
        weighted_sum
        / used_weight
    )



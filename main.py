import pandas as pd
import folium
from folium.plugins import MarkerCluster
from urllib.parse import quote

# 데이터 로드
df = pd.read_csv(
    "study_cafe_final_postprocessed.csv",
    encoding="utf-8-sig"
)

# 제외 카페 제거
df = df[
    df["presentation_grade"] != "제외"
].copy()

# 지도 생성
center_lat = df["latitude"].mean()
center_lon = df["longitude"].mean()

m = folium.Map(
    location=[center_lat, center_lon],
    zoom_start=14
)

cluster = MarkerCluster().add_to(m)

# 등급 색상
color_map = {
    "카공 성지": "blue",
    "추천": "green",
    "보통": "orange",
    "비추천": "red",
}

# 마커 생성
for _, row in df.iterrows():

    features = {
        "조용함": row["조용함_score"],
        "좌석": row["좌석_score"],
        "콘센트": row["콘센트_score"],
        "체류 적합성": row["체류 적합성_score"],
        "공부 적합성": row["공부 적합성_score"],
    }

    top3 = sorted(
        features.items(),
        key=lambda x: x[1],
        reverse=True
    )[:3]

    top3_text = "<br>".join(
        [f"• {name}" for name, _ in top3]
    )

    business_hours = row.get(
        "business_hours",
        "정보 없음"
    )

    if pd.isna(business_hours):
        business_hours = "정보 없음"

    rating = row.get(
        "rating",
        "정보 없음"
    )

    if pd.isna(rating):
        rating = "정보 없음"

    review_count = row.get(
        "review_count",
        0
    )

    if pd.isna(review_count):
        review_count = 0

    distance_text = (
        f"{int(row['straight_distance_m'])}m"
        if not pd.isna(
            row["straight_distance_m"]
        )
        else "정보 없음"
    )

    # 네이버 URL
    naver_url = row.get(
        "naver_map_url",
        None
    )

    if (
        pd.isna(naver_url)
        or not str(naver_url).startswith("http")
    ):
        naver_url = (
            "https://map.naver.com/p/search/"
            + quote(
                str(
                    row["kakao_name"]
                )
            )
        )

    popup_html = f"""
    <div style="
    width:220px;
    font-size:13px;
    line-height:1.4;
    text-align:center;
">

    <h4 style="
    margin-bottom:8px;
    text-align:center;
">
    <h3>[{row['kakao_name']}]</h3>

    🏆 <b>등급</b> :
    {row['presentation_grade']}<br>

    ⭐ <b>카공 점수</b> :
    {row['presentation_score']:.2f}<br><br>

    🕒 <b>영업시간</b> :
    {business_hours}<br><br>

    ⭐ <b>별점</b> :
    {rating}<br>

    📝 <b>리뷰 수</b> :
    {int(review_count)}개<br><br>

    📍 <b>용인대와 직선거리</b> :
    {distance_text}<br><br>

    <b>주요 장점 TOP3</b><br>
    {top3_text}<br><br>

    <a href="{naver_url}"
       target="_blank"
       style="
       background:#03C75A;
       color:white;
       padding:8px 12px;
       text-decoration:none;
       border-radius:5px;">
       네이버 검색
    </a>

    </div>
    """

    folium.Marker(
        location=[
            row["latitude"],
            row["longitude"]
        ],
        popup=folium.Popup(
            popup_html,
            max_width=400
        ),
        tooltip=(
            f"{row['kakao_name']} "
            f"({row['presentation_grade']})"
        ),
        icon=folium.Icon(
            color=color_map.get(
                row["presentation_grade"],
                "blue"
            )
        )
    ).add_to(cluster)

    # 용인대학교 표시
    folium.Marker(
        location=[37.2276, 127.1677],
        popup="용인대학교",
        tooltip="용인대학교",
        icon=folium.Icon(
            color="purple",
            icon="star",
            prefix="glyphicon"
        )
    ).add_to(m)


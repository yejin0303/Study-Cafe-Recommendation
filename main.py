def run_logistic_recommendation_model(
    score_path="study_cafe_final.csv",
    report_output="logistic_report.csv",
    importance_output="logistic_importance.csv",
):
    df = pd.read_csv(require_file(score_path))

    if "recommend_label" not in df.columns:
        raise ValueError(
            "recommend_label 컬럼이 필요합니다."
        )

    exclude_pattern = "|".join(
        re.escape(term)
        for term in EXCLUDED_CAFE_TERMS_FOR_FINAL
    )

    df = df[
        ~df["kakao_name"]
        .astype(str)
        .str.contains(
            exclude_pattern,
            regex=True,
            na=False,
        )
    ].copy()

    available_features = [
        col
        for col in MODEL_FEATURES
        if col in df.columns
    ]

    if len(available_features) < 3:
        print("추천 모델에 사용할 피처가 부족합니다.")
        return None

    model_df = df.dropna(
        subset=["recommend_label"]
    ).copy()

    for col in available_features:
        model_df[col] = pd.to_numeric(
            model_df[col],
            errors="coerce",
        )

        model_df[col] = model_df[col].fillna(
            model_df[col].median()
            if not model_df[col].dropna().empty
            else 0
        )

    label_counts = (
        model_df["recommend_label"]
        .value_counts()
    )

    if (
        model_df["recommend_label"].nunique() < 2
        or label_counts.min() < 2
    ):
        print(
            "추천/비추천 라벨 분포가 부족하여 "
            "Logistic Regression 학습을 건너뜁니다."
        )
        print(label_counts)
        return None

    X = model_df[available_features]
    y = model_df["recommend_label"].astype(int)

    model = LogisticRegression(
        max_iter=1000,
        class_weight="balanced",
        random_state=42,
    )

    cv = StratifiedKFold(
        n_splits=5,
        shuffle=True,
        random_state=42,
    )

    scoring = {
        "accuracy": make_scorer(
            accuracy_score
        ),
        "precision": make_scorer(
            precision_score,
            zero_division=0,
        ),
        "recall": make_scorer(
            recall_score,
            zero_division=0,
        ),
        "f1": make_scorer(
            f1_score,
            zero_division=0,
        ),
    }

    cv_result = cross_validate(
        model,
        X,
        y,
        cv=cv,
        scoring=scoring,
    )

    # 전체 데이터 학습
    model.fit(X, y)

    report = pd.DataFrame([
        {
            "model": "Logistic Regression",
            "n_samples": len(model_df),
            "n_features": len(available_features),
            "accuracy": round(
                cv_result["test_accuracy"].mean(),
                4,
            ),
            "precision": round(
                cv_result["test_precision"].mean(),
                4,
            ),
            "recall": round(
                cv_result["test_recall"].mean(),
                4,
            ),
            "f1": round(
                cv_result["test_f1"].mean(),
                4,
            ),
        }
    ])

    importance = pd.DataFrame({
        "feature": available_features,
        "coefficient": model.coef_[0],
        "importance": np.abs(
            model.coef_[0]
        ),
    }).sort_values(
        "importance",
        ascending=False,
    )

    report.to_csv(
        report_output,
        index=False,
        encoding="utf-8-sig",
    )

    importance.to_csv(
        importance_output,
        index=False,
        encoding="utf-8-sig",
    )

    print("Logistic Regression 5-Fold CV 성능")

    display(
        report.rename(
            columns={
                "model": "모델",
                "n_samples": "데이터 수",
                "n_features": "특성 수",
                "accuracy": "Accuracy",
                "precision": "Precision",
                "recall": "Recall",
                "f1": "F1",
            }
        )
    )

    print("\n피처 중요도 TOP 10")

    display(
        importance.head(10).rename(
            columns={
                "feature": "특성",
                "coefficient": "회귀계수",
                "importance": "중요도",
            }
        )
    )

    return report, importance


